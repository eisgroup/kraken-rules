package kraken.testproduct.domain;

public class PersonInfo extends Info {

    private String firstName;

    private String lastName;

    public PersonInfo() {
    }

    public PersonInfo(String firstName, String lastName, AddressInfo addressInfo) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.addressInfo = addressInfo;
    }

    private Integer age;

    private String occupation;

    private Boolean sameHomeAddress;

    private AddressInfo addressInfo;

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getOccupation() {
        return occupation;
    }

    public void setOccupation(String occupation) {
        this.occupation = occupation;
    }

    public Boolean getSameHomeAddress() {
        return sameHomeAddress;
    }

    public void setSameHomeAddress(Boolean sameHomeAddress) {
        this.sameHomeAddress = sameHomeAddress;
    }

    public AddressInfo getAddressInfo() {
        return addressInfo;
    }

    public void setAddressInfo(AddressInfo addressInfo) {
        this.addressInfo = addressInfo;
    }
}
